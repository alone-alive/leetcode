/**
 * Definition for singly-linked list.
 * public class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode(int x) { val = x; }
 * }
 */
class Solution {
    public ListNode removeNthFromEnd(ListNode head, int n) {
        ListNode start = head;
        int size = 0;
        while(start.next!=null){
            size++;
            start = start.next;
        }
        start = head;
        for(int i = size - n;i > 0;i--){
            start = start.next;
        }
        if(n == size + 1){
            return start.next;
        }
        start.next = start.next.next;
        return head;
    }
}
